class Command(BaseCommand):
    help = "Seed genres (Category/MajorGenre/MediumGenre). Idempotent."

    @transaction.atomic
    def handle(self, *args, **options):
        self.stdout.write(self.style.NOTICE("Seeding categories..."))
        categories_by_name = {}

        for name, slug, sort_order in CATEGORY_SEEDS:
            obj, _ = Category.objects.update_or_create(
                slug=slug,
                defaults={
                    "name": name,
                    "sort_order": sort_order,
                    "is_active": True,
                },
            )
            categories_by_name[name] = obj

        self.stdout.write(self.style.NOTICE("Seeding major genres..."))
        majors_by_key = {}  # (category_name, major_name) -> MajorGenre
        for cat_name, major_name, is_must_medium, sort_order in MAJOR_SEEDS:
            category = categories_by_name[cat_name]
            obj, _ = MajorGenre.objects.update_or_create(
                category=category,
                name=major_name,
                defaults={
                    "is_must_medium": is_must_medium,
                    "sort_order": sort_order,
                    "is_active": True,
                },
            )
            majors_by_key[(cat_name, major_name)] = obj

        self.stdout.write(self.style.NOTICE("Seeding medium genres..."))
        for cat_name, major_name, medium_name, sort_order in MEDIUM_SEEDS:
            major = majors_by_key[(cat_name, major_name)]
            MediumGenre.objects.update_or_create(
                major_genre=major,
                name=medium_name,
                defaults={
                    "sort_order": sort_order,
                    "is_active": True,
                },
            )

        self.stdout.write(self.style.SUCCESS("✅ seed_genres completed."))